#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nbr;

	nbr = (n < 0 ? (n * -1) : n);
	if (n < 0)
		ft_putchar('-');
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + 48);
}
